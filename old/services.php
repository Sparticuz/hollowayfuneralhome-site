<p>When the funeral, and sometimes even payment, have been arranged in advance, most of the decisions have been made, sparing uncertainty and confusion at a time when emotional stress may make decisions difficult.  If you decide to pre-pay part, or all of your arrangements today, you also make sure that spiraling inflation will never impact your family. The cost of the services and merchandise in your pre-need funeral account is guaranteed to cover the costs of inflation, no matter when the funeral service is needed from our funeral home. How do you make arrangements in advance? Simply by giving us a call, and one of our funeral directors will make arrangements to meet with you. We will make certain your pre-arrangement plans are kept on permanent file, eliminating much of the emotional and upsetting decision making at the time of need. Our funeral directors always stand ready to assist in coordinating and following through funeral plans made in advance with our funeral home.</p>

